    public ActualizarAutobus(int id) {
        initComponents();
        idAutobus = id;
        autobus = new Autobus();
        carroceria = new Carroceria();
        base = new Base();
        linea = new Linea();
        empleado = new Empleado();
        terminal = new Terminal();
        listaCarrocerias = carroceria.selectCarrocerias();
        listaBases = base.selectBases();
        listaTerminales = terminal.selectTerminales();
        listaLineas = linea.selectLineas();
        listaEmpleados = empleado.selectEmpleados();
        aut = autobus.selectAutobus(id);
        llenaCarrocerias();
        llenaBases();
        llenaLineas();
        llenaOperadores();
        CampoValor.setText(aut[1].toString());
        CampoAnios.setText(aut[2].toString());
        ListaCarrocerias.setSelectedIndex(Integer.parseInt(aut[3].toString())-1);
        ListaBases.setSelectedIndex(Integer.parseInt(aut[4].toString())-1);
        ListaLineas.setSelectedIndex(Integer.parseInt(aut[5].toString())-1);
        ListaOperadores.setSelectedIndex(opsel);
    }
    
    private void llenaCarrocerias(){
        // Consulta los datos de idioma
        for (Object[] e : listaCarrocerias) {
            // llena los datos de estados 
            ListaCarrocerias.addItem(e[1].toString()+" "+e[2].toString());
        }
    }
    
    private void llenaLineas(){
        // Consulta los datos de idioma
        for (Object[] e : listaLineas) {
            // llena los datos de estados 
            ListaLineas.addItem(e[1].toString());
        }
    }
    
    public void llenaBases() {
        for (Object[] t : listaBases) {
                Object[] ter = terminal.selectTerminal( Integer.parseInt(t[1].toString()) );
                ListaBases.addItem(ter[1].toString());
        }
    }
    
    private void llenaOperadores(){
        int i = 0;
        for (Object[] e : listaEmpleados) {
            // llena los datos de empleados cuyo puesto sea gerente
            if(Integer.parseInt(e[6].toString()) == 3)
            {
                ListaOperadores.addItem(e[0].toString()+" - "+e[1].toString());
                if( Integer.parseInt(e[0].toString()) == Integer.parseInt(aut[6].toString()) )
                    opsel = i;
                i++;
            }
        }
    }

