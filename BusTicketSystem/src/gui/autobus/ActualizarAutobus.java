    private void BotonActualizarAutobusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarAutobusActionPerformed
        Object[] e = new Object[7];
        e[0] = idAutobus;
        e[1] = CampoValor.getText().trim();
        e[2] = CampoAnios.getText().trim();
        e[3] = ListaCarrocerias.getSelectedIndex()+1;
        e[4] = ListaBases.getSelectedIndex()+1;
        e[5] = ListaLineas.getSelectedIndex()+1;
        int i = -1;
        for (Object[] empl : listaEmpleados) {
            if (Integer.parseInt(empl[6].toString()) == 3) {
                i++;
                if (i == ListaOperadores.getSelectedIndex()) {
                    e[6] = empl[0]; //OPERADOR_ID
                    break;
                }
            }
        }

        int r = autobus.updateAutobus(e);
        if( r > 0 )
        javax.swing.JOptionPane.showMessageDialog(this, "Se ha actualizado un autobus.","Aviso",2);
        else
        javax.swing.JOptionPane.showMessageDialog(this, "Error al intentar actualizar el autobus.","Aviso",2);
    }//GEN-LAST:event_BotonActualizarAutobusActionPerformed

