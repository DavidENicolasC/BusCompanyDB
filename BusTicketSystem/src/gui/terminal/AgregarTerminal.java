    private void BotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearActionPerformed
        if( camposLlenos() ) {
            int r;
            
            dir[1] = CampoMunicipio.getText();
            dir[2] = CampoColonia.getText();
            dir[3] = Integer.parseInt(CampoCodigoPostal.getText().trim());
            dir[4] = CampoCalle.getText();
            dir[5] = Long.parseLong(CampoNumero.getText().trim());
            dir[6] = Long.parseLong(CampoTelefono.getText().trim());
            dir[7] = CampoCorreo.getText();
            dir[8] = ListaEstados.getSelectedIndex()+1;

            r = direccion.insertDireccion(dir);
            
            if( r > 0 )
            {
                term[1] = NombreCampo.getText();    //NOMBRE
                term[2] = r;                        //DIRECCION_ID
                int i = -1;
                for (Object[] gerente1 : gerentes) {
                    if (Integer.parseInt(gerente1[6].toString()) == 2) {
                        i++;
                        if (i == ListaGerentes.getSelectedIndex()) {
                            term[3] = gerente1[0]; //GERENTE_ID
                            break;
                        }
                    }
                }
                
                r = terminal.insertTerminal(term);
                if( r > 0 )
                    javax.swing.JOptionPane.showMessageDialog(this, "Se ha creado una terminal.","Aviso",2);
                else
                    javax.swing.JOptionPane.showMessageDialog(this, "Error al intentar crear la terminal.","Aviso",2);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al intentar agregar la direccion.","Aviso",2);
            }
        }
    }//GEN-LAST:event_BotonCrearActionPerformed

