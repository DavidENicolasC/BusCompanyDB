    private void llenaEstados(){
        // Consulta los datos de idioma
        estados = estado.selectEstados();
        for (Object[] e : estados) {
            // llena los datos de estados 
            ListaEstados.addItem(e[1].toString());
        }
    }
    
    private void llenaGerentes(){
        // Consulta los datos de empleados
        gerentes = gerente.selectEmpleados();
        for (Object[] e : gerentes) {
            // llena los datos de empleados cuyo puesto sea gerente
            if(Integer.parseInt(e[6].toString()) == 2)
                ListaGerentes.addItem(e[0].toString()+" - "+e[1].toString());
        }
    }
    
