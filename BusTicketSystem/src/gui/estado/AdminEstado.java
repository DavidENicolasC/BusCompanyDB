    private void BotonEditarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarEstadoActionPerformed
        if (TablaEstados.getSelectedRow() < 0)
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una fila.","InformaciÃ³n",1);
        else
        {
            Object[] editEstado = estado.selectEstado(Integer.parseInt(TablaEstados.getValueAt(TablaEstados.getSelectedRow(), 0).toString()));

            ActualizarEstado actE = new ActualizarEstado(Integer.parseInt(editEstado[0].toString()));
            actE.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_BotonEditarEstadoActionPerformed

    private void BotonEliminarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarEstadoActionPerformed
        int r = estado.deleteEstado( Integer.parseInt(listaEstados[TablaEstados.getSelectedRow() ][0].toString()) ); //Elimina el registro
        switch (r) {
            case 0:
                javax.swing.JOptionPane.showMessageDialog(this, "Estado eliminado correctamente.","Aviso",1);
                break;
            case 1:
                javax.swing.JOptionPane.showMessageDialog(this, "No se puede borrar el estado.","Aviso",1);
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar borrar el estado.","Aviso",1);
                break;
        }
        listaEstados = estado.selectEstados();
        mostrarEstados();
    }//GEN-LAST:event_BotonEliminarEstadoActionPerformed

