    public void mostrarBases() {
        listaBases = base.selectBases();
        listaAutobuses = autobus.selectAutobuses();
        int[] num = new int[listaBases.length];
        for( int c=0; c<listaAutobuses.length; c++ )
            num[Integer.parseInt(listaAutobuses[c][4].toString())-1]++;
        Object[][] listaT = new Object[listaBases.length][6]; 
        for( int i = 0; i<listaBases.length; i++ )
        {
            listaT[i][0] = listaBases[i][0];
            term = terminal.selectTerminal(Integer.parseInt(listaBases[i][1].toString()));
            emp = empleado.selectEmpleado(Integer.parseInt(term[3].toString()));
            dir = direccion.selectDireccion(Integer.parseInt(term[2].toString()));
            est = estado.selectEstado(Integer.parseInt(dir[8].toString()));
            listaT[i][1] = est[1].toString();
            listaT[i][2] = term[1].toString();
            listaT[i][3] = emp[1].toString();
            listaT[i][4] = dir[1].toString();
            listaT[i][5] = num[i];
        }
        String[] T_BASES = {"ID","Estado", "Terminal", "Gerente", "Ciudad","No. de Autobuses"};
        int[][] cellAlignment = {{0,javax.swing.SwingConstants.LEFT}};
        int[][] cellSize = {{0,30},
                            {1,80},
                            {2,100},
                            {3,100},
                            {4,100},
                            {5,110}};
        // pone los datos en la tabla
        UtilsTable.llenaTabla(TablaBases,listaT, 
                T_BASES, cellAlignment, cellSize);
    }

