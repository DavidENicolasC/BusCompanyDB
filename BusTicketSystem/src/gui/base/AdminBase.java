    private void BotonEditarBaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarBaseActionPerformed
        if (TablaBases.getSelectedRow() < 0)
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una fila.","InformaciÃ³n",1);
        else
        {
            Object[] editBase = base.selectBase(Integer.parseInt(TablaBases.getValueAt(TablaBases.getSelectedRow(), 0).toString()));

            ActualizarBase actB = new ActualizarBase(Integer.parseInt(editBase[0].toString()));
            actB.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_BotonEditarBaseActionPerformed

    private void BotonEliminarBaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarBaseActionPerformed
        int r = base.deleteBase( Integer.parseInt(listaBases[TablaBases.getSelectedRow() ][0].toString()) ); //Elimina el registro
        switch (r) {
            case 0:
                javax.swing.JOptionPane.showMessageDialog(this, "Base eliminada correctamente.","Aviso",1);
                break;
            case 1:
                javax.swing.JOptionPane.showMessageDialog(this, "No se puede borrar el registro.","Aviso",1);
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar borrar la base.","Aviso",1);
                break;
        }
        listaBases = base.selectBases();
        mostrarBases();
    }//GEN-LAST:event_BotonEliminarBaseActionPerformed

