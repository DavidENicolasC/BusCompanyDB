    private void BotonEditarSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarSalidaActionPerformed
        if (TablaSalidas.getSelectedRow() < 0)
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una fila.","InformaciÃ³n",1);
        else
        {
            Object[] editTerminal = terminal.selectTerminal(Integer.parseInt(TablaSalidas.getValueAt(TablaSalidas.getSelectedRow(), 0).toString()));

            ActualizarSalida actT = new ActualizarSalida(Integer.parseInt(editTerminal[0].toString()));
            actT.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_BotonEditarSalidaActionPerformed

    private void BotonEliminarSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarSalidaActionPerformed
        int r = salida.deleteSalida( Integer.parseInt(listaSalidas[TablaSalidas.getSelectedRow() ][0].toString()) ); //Elimina el registro
        switch (r) {
            case 0:
                javax.swing.JOptionPane.showMessageDialog(this, "Salida eliminada correctamente.","Aviso",1);
                break;
            case 1:
                javax.swing.JOptionPane.showMessageDialog(this, "No se puede borrar la salida.","Aviso",1);
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar borrar la salida.","Aviso",1);
                break;
        }
        listaSalidas = salida.selectSalidas();
        mostrarSalidas();
    }//GEN-LAST:event_BotonEliminarSalidaActionPerformed

