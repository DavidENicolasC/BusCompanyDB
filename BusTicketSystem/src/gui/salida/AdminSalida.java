    public void mostrarSalidas() {
        // consulta los datos de las terminales
        Object[][] listaT = new Object[listaSalidas.length][9];
        for(int i=0; i<listaSalidas.length; i++)
        {
            termO = terminal.selectTerminal( Integer.parseInt( listaSalidas[i][6].toString() ) );
            termD = terminal.selectTerminal( Integer.parseInt( listaSalidas[i][7].toString() ) );
            aut = autobus.selectAutobus( Integer.parseInt( listaSalidas[i][8].toString() ) );
            listaT[i][0] = listaSalidas[i][0];
            listaT[i][1] = termO[1];
            listaT[i][2] = listaSalidas[i][1];
            listaT[i][3] = termD[1];
            listaT[i][4] = listaSalidas[i][2];
            listaT[i][5] = listaSalidas[i][3];
            listaT[i][6] = listaSalidas[i][4];
            listaT[i][7] = listaSalidas[i][5];
            listaT[i][8] = aut[0];
        }
        // configuraciÃ³n de la tabla
        String[] T_TERMINALES = {"ID","Origen", "Anden O.", "Destino", "Anden D.", 
            "Fecha/Hora", "Costo", "Duracion", "No. de Autobus"};
        int[][] cellAlignment = {{0,javax.swing.SwingConstants.LEFT}};
        int[][] cellSize = {{0,30},
                            {1,73},
                            {2,60},
                            {3,73},
                            {4,60},
                            {5,120},
                            {6,73},
                            {7,73},
                            {8,73}};
        // pone los datos en la tabla
        UtilsTable.llenaTabla(TablaSalidas,listaT, 
                T_TERMINALES, cellAlignment, cellSize);
    }
    
